# Bitwise XOR example in Python

# Input values
a = 6
b = 3

# Perform XOR
result = a ^ b

# Display results
print(f"a = {a} (binary: {a:04b})")
print(f"b = {b} (binary: {b:04b})")
print(f"a ^ b = {result} (binary: {result:04b})")

# Step-by-step binary calculation
# 6 -> 0110
# 3 -> 0011
# XOR -> 0101 (which is 5 in decimal)
